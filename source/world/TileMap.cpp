TileMap::TileMap() {
    // Copy static data into member array
    for (int row = 0; row < TILEMAP_H; row++) {
        for (int col = 0; col < TILEMAP_W; col++) {
            m_tiles[row][col] = s_mapData[row][col];
        }
    }
}

u8 TileMap::getTile(int tx, int ty) const {
    if (tx < 0 || ty < 0 || tx >= TILEMAP_W || ty >= TILEMAP_H) {
        return TILE_WALL; // treat out-of-bounds as solid
    }
    return m_tiles[ty][tx];
}

bool TileMap::isSolid(int tx, int ty) const {
    return getTile(tx, ty) == TILE_WALL;
}
